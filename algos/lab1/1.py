n = int(input())
A = list(map(int, input().split())) 

# Инициализация переменных
max_sum = -float('inf')  # Максимальная сумма (начальное значение - бесконечно малое)
current_sum = 0  # Текущая сумма
start = 0  # Индекс начала подпоследовательности
end = 0  # Индекс конца подпоследовательности
temp_start = 0  # Временный индекс начала подпоследовательности

for i in range(n):
    current_sum += A[i]  # Добавляем текущий элемент к сумме
    
    # Если текущая сумма больше максимальной, обновляем максимальную сумму и индексы
    if current_sum > max_sum:
        max_sum = current_sum
        start = temp_start
        end = i
    
    # Если текущая сумма стала отрицательной, начинаем новый подсчет с текущего элемента
    if current_sum < 0:
        current_sum = 0
        temp_start = i + 1  # Следующий элемент будет началом новой подпоследовательности

# Вывод результата
print(max_sum)  # Максимальная сумма подпоследовательности
print(start + 1, end + 1)  # Индексы начала и конца подпоследовательности (1-based)
